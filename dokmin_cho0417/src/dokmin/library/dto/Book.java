package dokmin.library.dto;

public class Book {
	private String book_code;
	private int book_tnor;
	private String book_state;
	private String book_manager;
	private String book_title;
	private String book_writer;
	private int book_genre;
	private int book_price;
	private String book_date;
	private String book_publisher;
	
	
	public String getBook_code() {
		return book_code;
	}
	public void setBook_code(String book_code) {
		this.book_code = book_code;
	}
	public int getBook_tnor() {
		return book_tnor;
	}
	public void setBook_tnor(int book_tnor) {
		this.book_tnor = book_tnor;
	}
	public String getBook_state() {
		return book_state;
	}
	public void setBook_state(String book_state) {
		this.book_state = book_state;
	}
	public String getBook_manager() {
		return book_manager;
	}
	public void setBook_manager(String book_manager) {
		this.book_manager = book_manager;
	}
	public String getBook_title() {
		return book_title;
	}
	public void setBook_title(String book_title) {
		this.book_title = book_title;
	}
	public String getBook_writer() {
		return book_writer;
	}
	public void setBook_writer(String book_writer) {
		this.book_writer = book_writer;
	}
	public int getBook_genre() {
		return book_genre;
	}
	public void setBook_genre(int book_genre) {
		this.book_genre = book_genre;
	}
	public int getBook_price() {
		return book_price;
	}
	public void setBook_price(int book_price) {
		this.book_price = book_price;
	}
	public String getBook_date() {
		return book_date;
	}
	public void setBook_date(String book_date) {
		this.book_date = book_date;
	}
	public String getBook_publisher() {
		return book_publisher;
	}
	public void setBook_publisher(String book_publisher) {
		this.book_publisher = book_publisher;
	}
	

}
