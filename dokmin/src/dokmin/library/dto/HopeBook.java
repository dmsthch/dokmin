package dokmin.library.dto;

public class HopeBook {
	private int hbook_no;
	private String member_id;
	private String hbook_name;
	private String hbook_writer;
	private String hbook_date;
	private String hbook_progress;
	public int getHbook_no() {
		return hbook_no;
	}
	public void setHbook_no(int hbook_no) {
		this.hbook_no = hbook_no;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getHbook_name() {
		return hbook_name;
	}
	public void setHbook_name(String hbook_name) {
		this.hbook_name = hbook_name;
	}
	public String getHbook_writer() {
		return hbook_writer;
	}
	public void setHbook_writer(String hbook_writer) {
		this.hbook_writer = hbook_writer;
	}
	public String getHbook_date() {
		return hbook_date;
	}
	public void setHbook_date(String hbook_date) {
		this.hbook_date = hbook_date;
	}
	public String getHbook_progress() {
		return hbook_progress;
	}
	public void setHbook_progress(String hbook_progress) {
		this.hbook_progress = hbook_progress;
	}
	

}
